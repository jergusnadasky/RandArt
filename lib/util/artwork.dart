class Artwork {
  final String title;
  final String artist;
  final String imageUrl;
  final String link;
  final String description;
  final String id;
  final String date;

  Artwork({
    required this.description,
    required this.title,
    required this.artist,
    required this.imageUrl,
    required this.link,
    required this.id,
    required this.date,
  });
}
